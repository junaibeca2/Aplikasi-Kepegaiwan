    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String id = jid.getText();
        String nama = jnama.getText();
        String jenis = (String)cmbjk.getSelectedItem();
        String alamat = jalamat.getText();
        String telepon = jtelp.getText();
        String email = jemail.getText();
        String jabatan = (String)cmbjabatan.getSelectedItem();
        
        try{
            Connection c = koneksi.getKoneksi();
            String sql = "INSERT INTO karyawan VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, id);
            p.setString(2, nama);
            p.setString(3, jenis);
            p.setString(4, alamat);
            p.setString(5, telepon);
            p.setString(6, email);
            p.setString(7, jabatan);
            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        } catch (SQLException e) {
            System.out.println("Data Tidak Tersimpan");
        }finally{
            autonumber();
            clear();
        } 
    }//GEN-LAST:event_btnsimpanActionPerformed
  private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        // TODO add your handling code here:
        btnsimpan.setEnabled(false);
        btnedit.setEnabled(true);
        btnhapus.setEnabled(true);
        
        int i = tblpegawai.getSelectedRow();
        if (i == -1) {
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        String nama = (String) model.getValueAt(i, 1);
        String jenis = (String) model.getValueAt(i, 2);
        String alamat = (String) model.getValueAt(i, 3);
        String telepon = (String) model.getValueAt(i, 4);
        String email = (String) model.getValueAt(i, 5);
        String jabatan = (String) model.getValueAt(i, 6);
        
        jid.setText(id);
        jnama.setText(nama);
        cmbjk.setSelectedItem(jenis);
        jalamat.setText(alamat);
        jtelp.setText(telepon);
        jemail.setText(email);
        cmbjabatan.setSelectedItem(jabatan);
    }                       
