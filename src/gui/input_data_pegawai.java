      public void cariData(){
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("ID Karyawan");
        tabel.addColumn("Nama Karyawan");
        tabel.addColumn("Jenis Kelamin");
        tabel.addColumn("Alamat");
        tabel.addColumn("Telepon");
        tabel.addColumn("Email");
        tabel.addColumn("Jabatan");
        
        try {
            java.sql.Connection c = koneksi.getKoneksi();
            String sql = "Select * from karyawan where id_karyawan like '%" + jcari.getText() + "%'" +
                    "or nama_karyawan like '%" + jcari.getText() + "%'";
            java.sql.Statement stat = c.createStatement();
            java.sql.ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {                
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),
                });
            }
            tblpegawai.setModel(tabel);
        } catch (SQLException e) {
            System.out.println("Cari Data Error");
        }finally{
        }
      }
      
