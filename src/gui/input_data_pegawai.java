     private void autonumber(){
        try {
            java.sql.Connection c = koneksi.getKoneksi();
            java.sql.Statement s = c.createStatement();
            String sql = "SELECT * FROM karyawan ORDER BY id_karyawan DESC";
            java.sql.ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("id_karyawan").substring(2);
                String KR = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if (KR.length()==1) 
                    {Nol = "00";}
                else if(KR.length()==2)
                    {Nol = "0";}
                else if(KR.length()==3)
                    {Nol = "";}
                
                jid.setText("KR" + Nol + KR);  
            }else{
                jid.setText("KR001");
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("autonumber error");
        }
     }
     
